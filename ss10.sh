#\n new line
echo "Hey world, \nwhats up?"
#\r over write
echo "Hey world, \rwhats up?"
#\t tab
echo "Hey world, \twhats up?"
#\b erases the character before, like "back"
echo "Hey world, \b\bwhats up?"
#\033[033 bolded
echo "Hey world,\033[1m whats up?\033[0m"
#reverse video format
echo "\033[7mHey world, whats up?\033[0m"
